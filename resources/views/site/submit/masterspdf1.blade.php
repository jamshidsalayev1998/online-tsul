<div style="">
    <h4 style="text-align: center;">2-боб. Ҳужжатларни тақдим этиш
        ва магистратурага ўқишга қабул қилиш
    </h4>
    <p>
        7. Айрим тоифадаги абитуриентлар томонидан ТДЮУ магистратурасига ўқишга кириш учун ҳужжат топшириш мазкур Низом талабларини инобатга олган ҳолда Ўзбекистон Республикаси фуқаролари учун белгиланган тартибда амалга оширилади.
    </p>
    <p>
        8. Айрим тоифадаги абитуриентлар томонидан ТДЮУ магистратурасига ўқишга кириш учун ҳужжат топшириш Ўзбекистон Республикаси фуқаролари учун белгиланган муддатларда (ҳар йили 1 июлдан 30 июлгача)Интернет жаҳон ахборот тармоғи орқали ТДЮУ қабул комиссиясига мазкур Низомнинг иловасида келтирилган намунадаги ариза ҳамда қуйидаги ҳужжатлар электрон шаклда тақдим этилади:
        паспорт (ID-карта) ёки унинг ўрнини босувчи ҳужжат;
        олий маълумот тўғрисидаги диплом (ёки дипломдан кўчирма – Ўзбекистон Республикаси олий таълим муассасаларини давлат гранти бўйича ўқиб тугатганлар учун) ҳамда унга илова.
        тегишли давлат органи биринчи раҳбарининг тавсияси асосида базавий олий юридик маълумот ҳамда давлат ҳокимияти ва бошқарувининг марказий органларида юридик мутахассислик бўйича камида беш йил иш стажига эга бўлган раҳбар ходимлар учун ўрнатилган тартибда имзоланган ва давлат органининг бланкида расмийлаштирилган, рақами ва берилган вақти акс эттирилган ҳамда электрон меҳнат дафтарчасида кўрсатилган маълумотлар илова қилинган тегишли давлат органи биринчи раҳбарининг тавсияси (мазкур Низомнинг 3-бандида назарда тутилган асосга кўра ҳужжат топширилганда);
        базавий олий юридик маълумотга эга, юридик техникумларда камида уч йил ишлаган педагог ва раҳбар ходимлар учун ўрнатилган тартибда имзоланган ва юридик техникум бланкида расмийлаштирилган, рақами ва берилган вақти акс эттирилган ҳамда электрон меҳнат дафтарчасида кўрсатилган маълумотлар илова қилинган юридик техникум директорининг тавсияси (мазкур Низомнинг 5-бандида назарда тутилган асосга кўра ҳужжат топширилганда).

    </p>
    <p>
        9. Айрим тоифадаги абитуриентларни ТДЮУ магистратурасига ўқишга қабул қилишни ташкил этиш, уларнинг ҳужжатларини рўйхатдан ўтказиш тегишли ўқув йили учун тасдиқланадиган ТДЮУ қабул комиссияси зиммасига юкланади.
        ТДЮУ қабул комиссияси айрим тоифадаги абитуриентлар томонидан ТДЮУ магистратурасига ўқишга кириш учун топширилган ҳужжатларнинг тўлиқлиги ва тўғрилигини беш иш куни давомида ўрганади ҳамда унинг натижаси бўйича абитуриентга хабар беради.
        Абитуриентлар томонидан тақдим этилган ҳужжатларни ўрганиш вақтидакамчиликлар аниқланса, ТДЮУ қабул комиссияси томонидан давлат ҳокимияти ва бошқарувининг марказий органлари, юридик олий таълим муассасалари, юридик техникумлар ҳамда абитуриентдан қўшимча маълумотлар сўраб олиниши мумкин.

    </p>
    <p>
        10. Айрим тоифадаги абитуриентларнинг ҳужжатларини ўрганиш давомида абитуриентда тегишли имтиёз мавжуд эмаслиги аниқланса, унинг ҳужжатлари кўрмай қолдирилади ва бу ҳақда абитуриентга хабар берилади.
        Айрим тоифадаги абитуриентлар томонидан ТДЮУ магистратурасига ўқишга кириш учун ҳужжат топширишнинг белгиланган муддатларидан
        кеч қолиб топширилган ҳужжатлар кўриб чиқилмайди.
        Ҳужжатларда, шу жумладан, электрон шаклдаги маълумотларда қалбакилаштириш ёки сохталаштириш ҳолатлари аниқланганда, абитуриентнинг ҳужжатлари кўриб чиқилмайди (магистратурага қабул қилинган бўлса ТДЮУ магистратураси талабалари сафидан четлаштирилади), ҳужжатлар эса, белгиланган тартибда ҳуқуқни муҳофаза қилиш органларига юборилади.

    </p>
    <p>11. Айрим тоифадаги абитуриентларни ТДЮУ магистратурасига ўқишга қабул қилиш масаласини кўриб чиқиш ва тегишли қарор қабул қилиш бўйича ТДЮУ қабул қомиссияси томонидан Давлат комиссиясига қабул йилининг 20 августга қадар маълумот киритилади.
    </p>
    <p>
        12. Давлат комиссиясининг абитуриентларни ўқишга қабул қилиш тўғрисидаги қарори 30 августга қадар қабул қилинади ва бу ҳақида ТДЮУга хабар берилади.
        Ўқишга қабул килишга тавсия этилган абитуриентлар рўйхати ТДЮУ веб-сайтига жойлаштирилади.
    </p>
    <p>
        13. Давлат комиссияси томонидан қабул қилинган қарор асосида абитуриентларни ТДЮУ магистратурасига тўлов-контракт асосида қабул қилиш мақсадида ТДЮУ ва юридик ёки жисмоний шахслар ўртасида контракт (шартнома) тузилади ва унда контракт тўлови суммасини белгиланган муддатда тўлаш шарти кўрсатиб ўтилади.

    </p>
    <p>
        14. Белгиланган намунадаги шартнома бланкалари ТДЮУ магистратурасига ўқишга қабул қилиш тўғрисида Давлат комиссияси қарори қабул қилингандан сўнг берилади ҳамда ТДЮУ ректори ва бош бухгалтери имзоси билан юридик шахслар учун уч нусхада ва жисмоний шахслар учун икки нусхада расмийлаштирилади.

    </p>
    <p>
        15. Давлат комиссиясининг абитуриентларни ТДЮУ магистратурасига ўқишга қабул қилиш бўйича тавсия этилганлиги тўғрисидаги қарори қабул қилинганидан сўнг уч иш куни мобайнида мазкур Низомнинг 8-бандида назарда тутилган ҳужжатларнинг асл нусхалари абитуриент томонидан ТДЮУ қабул комиссиясига Магистратура бўлимига тақдим этилади.
    </p>
