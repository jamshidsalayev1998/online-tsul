    <h4 style="text-align: center;">3-боб. Магистратура битирувчиларини тақсимотга мувофиқ
        узлуксиз ишлаб бериш учун тақсимлаш

    </h4>
<p>
    16. Мазкур Низомнинг 4 ва 5-бандларига мувофиқ ўқишга қабул қилинган магистратура битирувчиларини адлия органлари ва муассасалари, ТДЮУ, юридик факультетлар, юридик техникумлар ва ТДЮУ қошидаги академик лицейда ишлашга тақсимлаш учун адлия вазирининг буйруғи билан тақсимот комиссияси (кейинги ўринларда – тақсимот комиссияси) тузилади.
    Тақсимот комиссиясига ТДЮУ ректори раислик қилади ҳамда унинг таркибига адлия органлари ва муассасалари, юридик факультетлар, юридик техникумлар ва ТДЮУ қошидаги академик лицей вакиллари киритилиши мумкин.

</p>
    <p>
        17. ТДЮУ магистратура битирувчиларини узлуксиз ишлаб бериш учун тақсимлаш ҳар йили тақсимот комиссияси томонидан адлия органлари
        ва муассасалари, ТДЮУ, юридик факультетлар, юридик техникумлар
        ва ТДЮУ қошидаги академик лицей томонидан тақдим этилган буюртмаларга асосан амалга оширилади.
        Тақсимлаш жараёнида тақсимот комиссияси магистратура битирувчисининг рейтинг натижалари, оилавий ҳолати ва таклиф этилган иш ўрнида ишлаш хоҳишини ҳисобга олади. Энг юқори ўзлаштириш кўрсаткичи (86 фоиз ва ундан юқори)га эга бўлган магистратура битирувчилари
        ўз танлови бўйича мавжуд иш ўринларига ишга жойлашишда биринчи навбатда талабгор бўлади.

    </p>
<p>
    17. ТДЮУ магистратура битирувчиларини узлуксиз ишлаб бериш учун тақсимлаш ҳар йили тақсимот комиссияси томонидан адлия органлари
    ва муассасалари, ТДЮУ, юридик факультетлар, юридик техникумлар
    ва ТДЮУ қошидаги академик лицей томонидан тақдим этилган буюртмаларга асосан амалга оширилади.
    Тақсимлаш жараёнида тақсимот комиссияси магистратура битирувчисининг рейтинг натижалари, оилавий ҳолати ва таклиф этилган иш ўрнида ишлаш хоҳишини ҳисобга олади. Энг юқори ўзлаштириш кўрсаткичи (86 фоиз ва ундан юқори)га эга бўлган магистратура битирувчилари
    ўз танлови бўйича мавжуд иш ўринларига ишга жойлашишда биринчи навбатда талабгор бўлади.

</p>
<p>
    18. Магистратура битирувчиларини узлуксиз ишлаб бериш учун тақсимлаш ҳар йили, қоида тариқасида, магистратура битирувчиси ўқишни тамомлагандан сўнг, бир ойдан кечиктирмай амалга оширилади.
    Тақсимот комиссияси томонидан магистратура битирувчисига адлия органлари ва муассасалари, ТДЮУ, юридик факультетлар, юридик техникумлар ва ТДЮУ қошидаги академик лицейда ишлаш учун тегишли тартибда йўлланма берилади. Бир вақтнинг ўзида битирувчига йўлланма билан бирга ишга келгани ва жойлашгани ҳақида тўлдириладиган ҳужжат намунаси ҳам берилади.
    Ишга йўлланма берилганидан тегишли ташкилот – иш берувчи, ТДЮУ ва магистратура битирувчиси ўртасида узлуксиз ишлаб беришнинг аниқ шартларини ҳамда битирувчининг мажбуриятларини аниқ белгилаб берувчи уч томонлама шартнома уч нусхада тузилади (магистратура битирувчиси ТДЮУга ишга тақсимланган ҳоллар бундан мустасно).
    Шартнома намунаси белгиланган тартибда Адлия вазирлиги томонидан тасдиқланади.
</p>
</div>
<div style="">
    <h4 style="text-align: center;">3-боб. Магистратура битирувчиларини ишга қабул қилиш
        ва уларнинг меҳнат фаолияти
    </h4>
    <p>
        20. Мазкур Низомнинг 4 ва 5-бандларига мувофиқ ўқишга қабул қилинган магистратура битирувчилари ишга тақсимланганидан кейин бир ойдан ошмаган муддатда уч томонлама шартномада кўрсатилган иш жойида ҳозир бўлишлари ва меҳнат фаолиятини бошлашлари шарт.
        Иш берувчи уч томонлама шартномага ва ишга йўлланмага мувофиқ битирувчи билан қонун ҳужжатларида белгиланган тартибда меҳнат шартномаси тузади.
        Иш берувчи битирувчи томонидан тақдим этилган битирувчининг ишга келгани ва жойлашгани ҳақидаги ҳужжатни тўлдиради ва кейинчалик уни беш кун муддат ичида ТДЮУга юборади.
        ТДЮУ магистратура битирувчисининг ишга келгани ва жойлашгани ҳақидаги ҳужжат асосида ҳар бир магистратура битирувчисининг ҳисобини юритади.

    </p>
<p>
    21. Магистратура битирувчисининг уч йиллик узлуксиз ишлаб бериш муддати қуйидагиларни ўз ичига олади:
    йўлланма берилган ташкилотларда ва лавозимларда ишлаш муддати;
    давлат ҳокимиятининг марказий органларидаги хизмат фаолияти даври (техник, хизмат кўрсатувчи ва ишлаб чиқариш билан боғлиқ бўлинмалардаги хизмат фаолият даври бундан мустасно);
    муддатли ҳарбий хизмат ва сафарбарлик чақируви резервидаги ҳарбий хизматни ўташ муддатлари.
    Ижтимоий меҳнат таътили муддати уч йиллик узлуксиз ишлаб бериш муддатига кирмайди.
    Тақсимот бўйича узлуксиз ишлаб беришга юборилган магистратура битирувчиси ишга жойлашгандан сўнг, иш берувчи унга тегишли меҳнат шароитлари яратади, зарур ҳолларда турар жой билан таъминлаш чораларини кўради.
</p>
    <p>
        22. Магистратура битирувчисининг уч йиллик узлуксиз ишлаб бериш муддати тугагандан сўнг ишни давом эттириш ҳақидаги масала меҳнат қонунчилигига мувофиқ иш берувчи ва ходим ўртасида ўзаро келишувга асосан ҳал этилади.
    </p>
    <p>
        23. Магистратура битирувчиси узлуксиз ишлаб бериш муддати давомида у тақсимот бўйича юборилган ташкилот тугатилса, меҳнат шартномаси (контракт) иш берувчининг ташаббуси билан бекор қилинса, магистратура битирувчиси ТДЮУни бу ҳақда 5 кун ичида хабардор қилиши шарт.
        Магистратура битирувчисининг мурожаати бўйича тақсимот комиссияси мазкур Низомнинг 8-бандига мувофиқ бошқа ташкилотларда мавжуд бўш иш ўринларига улар тақдим этган талабномалар асосида битирувчини тақсимлашни амалга оширади.

    </p>
    <p>
        24. Ушбу Низомга мувофиқ узлуксиз ишлаб бериш учун тақсимланган магистратура битирувчисида узлуксиз ишлаб бериш жойини ўзгартириш учун асосли сабаблар вужудга келган тақдирда (никоҳ тузиш, доимий яшаш учун республиканинг бошқа ҳудудига кўчиб кетиш ва бошқалар) магистратура битирувчиси тақсимот комиссиясига узлуксиз ишлаб бериш жойини ўзгартириш учун тегишли тасдиқловчи ҳужжатларни тақдим этган ҳолда мурожаат қилишга ҳақли.
        Тақсимот комиссияси ўн кун ичида магистратура битирувчисининг мурожаатини ва тақдим этилган тасдиқловчи ҳужжатларни кўриб чиқиш натижалари бўйича магистратура битирувчисига мажбурий ишлаб бериш учун бошқа жойни ушбу Низом талабларига мувофиқ белгилаб беради.

    </p>
    <p>
        25. ТДЮУ магистратура битирувчисига магистр даражаси тўғрисидаги дипломининг асл нусхасини у узлуксиз ишлаб бериш муддатини тамомлаганидан сўнг бир ҳафта муддатда меҳнат дафтарчасидаги ёзув
        ва битирувчи узлуксиз ишлаб берган ташкилотнинг ёзма тасдиғи асосида беради.

    </p>
</div>
<div style="">
    <h4 style="text-align: center;">4-боб. Якунловчи қоидалар
    </h4>
<p>
    26. Магистратура битирувчиси тақсимот бўйича сабабсиз уч йил узлуксиз ишлаб беришдан бош тортган ҳолларда, ўқишни битирган вақтида ТДЮУда амалда бўлган шартнома суммасининг беш баробари миқдорида ТДЮУга компенсация тўлайди.
</p>
    <p>
        27. Мазкур Низомга мувофиқ узлуксиз ишлаб беришдан бош тортган магистратура битирувчиларига магистр даражаси тўғрисидаги дипломнинг асл нусхаси улар белгиланган компенсацияни тўлиқ миқдорда тўлагандан сўнг берилади.

    </p>
<p>
    28. Узлуксиз ишлаб бериш муддати давомида меҳнат шартномаси (контракт) магистратура битирувчисининг ташаббуси билан бекор қилинса, иш берувчи томонидан бу ҳақда уч иш куни мобайнида ТДЮУга хабар берилади.
    ТДЮУ магистратура битирувчиси томонидан компенсация тўлашни таъминлаш, шунингдек, заруратга кўра, компенсацияни суд тартибида ундириш чорасини кўради.
    Бунда, компенсация миқдори магистратура битирувчиси томонидан ишлаб берилган вақтга пропорционал тарзда ҳисоблаб чиқилади.

</p>
<p>
    29. Магистратура битирувчиси узлуксиз ишлаб беришдан бош тортиши билан боғлиқ ТДЮУга тўлайдиган компенсация тўлиқ ҳажмда ТДЮУни ривожлантириш жамғармаси ҳисобига ўтказилади.

</p>
<p>
    30. Мазкур Низомга мувофиқ магистратура битирувчиси меҳнатга лаёқатсизлиги туфайли узлуксиз ишлаб беролмаган ҳолатларда унинг кейинги меҳнат фаолияти тўғрисидаги қарор тақсимот комиссияси томонидан белгиланган тартибда қабул қилинади.
    Меҳнат лаёқатини йўқотгани сабабли (тасдиқловчи ҳужжатлар мавжуд бўлганда) узлуксиз ишлаб бериш имкониятига эга бўлмаган битирувчилар, шунингдек, давлат ҳокимиятининг марказий органларида хизмат фаолиятини (техник, хизмат кўрсатувчи ва ишлаб чиқариш билан боғлиқ бўлинмалардаги хизмат фаолияти бундан мустасно) амалга ошираётган битирувчилар тақсимот комиссияси қарори асосида компенсация тўлашдан озод этилади.

</p>
<p>
    31. Ушбу Низом талаблари бузилишида айбдор бўлган шахслар қонун ҳужжатларида белгиланган тартибда жавоб берадилар.

</p>
<p>
    32. Ушбу Низомни қўллашда юзага келадиган низолар қонун ҳужжатларида белгиланган тартибда ҳал этилади.
</p>
</div>


