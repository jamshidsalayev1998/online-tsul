<div style="">
    <h4 style="text-align: center;">1-боб. Умумий қоидалар</h4>
    <p>
        1. Ушбу Низом Тошкент давлат юридик университети
        (кейинги ўринларда – ТДЮУ) магистратурасига айрим тоифадаги абитуриентларни кириш тест синовларисиз қабул қилиш, уларни тақсимотга мувофиқ узлуксиз ишлаб бериш учун тақсимлаш ҳамда ишга қабул қилиш тартибинибелгилайди.
    </p>
    <p>
        2. Ушбу Низомдақуйидагиасосийтушунчаларданфойдаланилади:
        а) айрим тоифадаги абитуриентлардеганда қуйидагилар тушунилади:
        тегишли давлат органи биринчи раҳбарининг тавсияси асосида базавий олий юридик маълумот ҳамда давлат ҳокимияти ва бошқарувининг марказий органларида юридик мутахассислик бўйича камида беш йил иш стажига эга бўлган раҳбар ходимлар;
        “юриспруденция” таълим йўналиши бўйича бакалавриатни “имтиёзли” диплом билан тугатган битирувчилар;
        базавий олий юридик маълумотга эга, юридик техникумларда камида уч йил ишлаган педагог ва раҳбар ходимлар;
        б) давлат ҳокимияти ва бошқарувининг марказий органларининг раҳбар ходимларидеганда қуйидагилар тушунилади:
        Ўзбекистон Республикаси Президенти Администрацияси, Олий Мажлисива Вазирлар Маҳкамасининг таркибий бўлинмалари раҳбарлари ҳамда бошқарув кадрлари;
        республика давлат ҳокимияти ва бошқаруви органлари, шунингдек, бошқа марказий давлат органларининг (Ўзбекистон Республикаси Бош прокуратураси, Давлат хавфсизлик хизмати, Миллий гвардияси, Марказий сайлов комиссияси, Марказий банки, Ҳисоб палатаси, Фанлар академияси, Инсон ҳуқуқлари бўйича Ўзбекистон Республикаси Миллий маркази) раҳбари, унинг ўринбосарлари, марказий аппаратдаги таркибий бўлинма (техник, хизмат кўрсатувчи ва ишлаб чиқариш билан боғлиқ бўлинмалардан ташқари) бошлиқлари.
        <del><mark>республика давлат ҳокимияти ва бошқаруви органлари, шунингдек, бошқа марказий давлат органлари ҳудудий (Қорақалпоғистон Республикаси, Тошкент шаҳри ва вилоятлар) бўлинмаларининг раҳбари ва уларнинг ўринбосарлари</mark></del>.
    </p>
    <p>
        3. Тегишли давлат органи биринчи раҳбарининг (Ўзбекистон Республикаси Президенти Администрацияси ходимлари учун – Ўзбекистон Республикаси Президентининг тегишли маслаҳатчилари ва департамент раҳбарлари, Вазирлар Маҳкамаси аппарати ходимлари учун – Вазирлар Маҳкамаси аппарати бошқарувчиси ҳам) тавсияси асосида базавий олий юридик маълумот ҳамда давлат ҳокимияти ва бошқарувининг марказий органларида <span style="color: red">(марказий аппаратида)</span> юридик мутахассислик бўйича камида беш йил иш стажига эга бўлган раҳбар ходимлар ТДЮУ магистратурасига масофавий, сиртқи ва кечки таълим шакллари асосида тест синовларисиз, тўлов-контракт асосида, қабул параметрларидан ташқари, ишлаб чиқаришдан ажралмаган ҳолда ўқишга қабул қилинади.
    </p>
    <p>
        4. “Юриспруденция” таълим йўналиши бўйича бакалавриатни “имтиёзли” диплом билан тугатган битирувчиларни ТДЮУ магистратурасига қабул қилиш тест синовларисиз, тўлов-контракт асосида, ўқиш қийматининг 50 фоизини тўлаб, қабул параметрларидан ташқари, ўқишни тугатгандан сўнг ТДЮУ, юридик факультетлар, юридик техникумлар ва ТДЮУ қошидаги академик лицейда камида уч йил узлуксиз ишлаш мажбуриятини олган ҳолда амалга оширилади.
        Мазкур бандда назарда тутилган тартиб 2020/2021 ўқув йили ва ундан кейинги йилларда “Юриспруденция” таълим йўналиши бўйича бакалавриатни “имтиёзли” диплом билан тугатган битирувчиларга нисбатан татбиқ этилади.
    </p>
    <p>
        5. Базавий олий юридик маълумотга эга, юридик техникумларда камида уч йил ишлаган педагог ва раҳбар ходимлар ТДЮУ магистратурасига кириш тест синовларисиз, тўлов-контракт асосида, қабул параметрларидан ташқари, ўқишни тугатгандан сўнг тақсимот бўйича адлия органлари ва муассасаларида, биринчи навбатда, юридик техникумларда камида уч йил узлуксиз ишлаш мажбуриятини олган ҳолда ўқишга қабул қилинади.
    </p>
    <p>
        6. Мазкур Низомнинг 4 ва 5-бандларига мувофиқ ўқишга қабул қилинган шахслар контракт (шартнома) тузишдан олдин ўқишни тугатгандан сўнг тақсимот бўйича ишлаш мажбурияти шартлари ҳамда унинг бажарилмаслиги оқибатида юзага келиши мумкин бўлган жавобгарлик бўйича огоҳлантириш хати орқали таништирилади.
    </p>
